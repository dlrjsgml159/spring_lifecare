package com.spring.lifecare.vo;

public class FooterBoard_proofCost {
	
	//멤버변수
	private String name;
	private String code;
	private String section;
	private String cost;
	private String min_cost;
	private String max_cost;
	private String issue;
	
	//setter,getter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getMin_cost() {
		return min_cost;
	}
	public void setMin_cost(String min_cost) {
		this.min_cost = min_cost;
	}
	public String getMax_cost() {
		return max_cost;
	}
	public void setMax_cost(String max_cost) {
		this.max_cost = max_cost;
	}
	public String getIssue() {
		return issue;
	}
	public void setIssue(String issue) {
		this.issue = issue;
	}
	
	

}
